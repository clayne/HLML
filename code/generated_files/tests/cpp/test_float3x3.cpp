TEMPER_PARAMETRIC( Test_float3x3_inverse, TEMPER_FLAG_SHOULD_RUN, const float3x3& mat, const float3x3& expectedAnswer )
{
	float3x3 inversed = inverse( mat );
	TEMPER_CHECK_TRUE( inversed == expectedAnswer );

	// test inversed matrix multiplied by the original equals identity
	float3x3 identityMat;
	identity( identityMat );

	float3x3 matMultipliedByItsInverse = mul( mat, inversed );
	TEMPER_CHECK_TRUE( matMultipliedByItsInverse == identityMat );
}

